    private void signUpBtnPanelMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_signUpBtnPanelMouseReleased
        try {
            if (controller.signUp()) {
                controller.getView().MessageLabel.setText("You have successfully registered");
                controller.getView().MessageLabel.setForeground(new Color(102, 255, 102));
                controller.getView().MessageLabel.setVisible(true);
                controller.changePanel(controller.getSignIn());
            } else {
                controller.getView().MessageLabel.setText("Please fill all the blanks");
                controller.getView().MessageLabel.setForeground(new Color(255,204,51));
                controller.getView().MessageLabel.setVisible(true);
            }
        } catch (Exception ex) {
            controller.getView().MessageLabel.setText("Error: This user already exists");
            controller.getView().MessageLabel.setForeground(new Color(253, 0, 101));
            controller.getView().MessageLabel.setVisible(true);
        }
    }//GEN-LAST:event_signUpBtnPanelMouseReleased

