    private void signIn() {
        try {
            controller.initiatePrincipal(controller.login());
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
            controller.getView().MessageLabel.setText("Error: Incorrect data");
            controller.getView().MessageLabel.setForeground(new Color(253, 0, 101));
            controller.getView().MessageLabel.setVisible(true);
        }
    }

